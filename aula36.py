"""
Operadores de Atribuição
= += -= /= //= **= %=
Pode ser usado não só com números mas como strings também, atribuindo operação a frases e não só números
"""

contador = 0 #"Contador" se torna o número base inicial para qualquer operação.
contador += 10 #contador = contador + 10
print(contador)
contador -= 2 #contador = contador - 2
print(contador)
contador *= 10 #contador = contador * 10
print(contador)
contador /= 8 #contador = contador / 8
print(contador)
contador //= 5 #contador = contador // 5
print(contador)
contador **= 3 #contador = contador ** 3
print(contador)
contador %= 2 #contador = contador % 2
print(contador)