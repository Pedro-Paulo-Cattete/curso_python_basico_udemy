# Boolean = Tipo de dado bool para questionar o programa
# Ao questionar algo em um programa só existem duas respostas possíveis sim (true) ou não (false)
# Existem vários operadores para questionar, exemplo: == que questiona se um valor é igual ao outro

# Operador ==:
print(10 == 10) # Sim (true)
print(10 == 11) # Não (false)
print(type(10 == 10)) # Sim (true)
print(type(10 == 11)) # Não (false)


