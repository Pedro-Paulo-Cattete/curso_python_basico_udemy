# Python é uma linguagem dinâmica e forte (Forte: Tem de estar escrita de forma correta para poder funcionar)

# Conversão de tipos (Coerção, Convertion, Typecasting, Coercion)
# É o ato de converter um tipo em outro
# Classes imutáveis e primitivas:
# Str (String/Texto), int (Números inteiros), float (Números racionais), bool (Sim ou Não)

print (1 + 1) # Número soma em um número unitário (2)
print ("a" + "b") # Letras somam uma a outra (ab)

# Coerção de tipo (type também avisa ao programa qual tipo você está usando):
print (int('1'), type (int('1')))
print (int('1') + 1)
print (float('1') + 1)
print (bool(' '))
print (str(11) + 'b')