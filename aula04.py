# Funções: se está em letras minusculas + parenteses é função
# Diferenças entre números: int (Inteiros) e float (Qualquer número)
# Para separar casas decimais é utilizado o "." Exemplo: para exibir 1,1 deve ser escrito 1.1
# Função type é para saber o tipo de dado
# Exemplos de int:
print(11)
print(-11)
print(0)

#Exemplos de float (Deve criar uma função dentro de print para o sistema entender que são decimais): 
print(1.1, 11.11) #Errado, sem tipografia
print(0.0, -1.5) #Certo, indicando o tipo de texto

#Definindo tipos:
print(type('Otavio'))
print(type(0))
print(type(1.1), type(-1.1), type (0.0))